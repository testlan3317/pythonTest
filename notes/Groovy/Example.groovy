class Example{
    static void main(String[] args){
        println("Hello_Groovy");
        int a = 2
        switch(a){
            case 1:
                println("The value of a is one");
                break;
            case 2:
                println("The value of a is Two");
                break;
            case 3:
                println("The value of a is Three");
                break;
            case 4:
                println("The value of a is Four");
                break;
            default:
                println("The value is unknown");
                break;
        }
    }
}

